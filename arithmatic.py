def add(no1,no2):  # no1 and no2 denote the numbers on whom the arithmatic function is to be performed
   sum = no1 + no2
   print("the sum of", no1, "and", no2, "is", sum)
def subtract(no1,no2):
   subtraction = no1 - no2
   print(no2, "subtracted from", no1, "is", subtraction)
def multiply(no1,no2):
    product = no1*no2
    print("the product of", no1, "and", no2, "is", product)
def divide(no1,no2):
    division = no1/no2
    print(no1,"divided by",no2,"is",division)
